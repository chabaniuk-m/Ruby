def area(nodes)
  s = 0
  (0...nodes.length).each { |i|
    x1 = nodes[i][0]
    x2 = i + 1 >= nodes.length ? nodes[0][0] : nodes[i + 1][0]
    y1 = nodes[i][1]
    y2 = i + 1 >= nodes.length ? nodes[0][1] : nodes[i + 1][1]
    s += (x1 + x2) * (y2 - y1)
  }
  s.abs / 2.0
end

